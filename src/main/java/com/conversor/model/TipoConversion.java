package com.conversor.model;

/**
 * Enumeración que define los tipos de conversión soportados por la aplicación
 */
public enum TipoConversion {
    /**
     * Conversión entre monedas fiduciarias (divisas)
     */
    MONEDA,
    
    /**
     * Conversión entre criptomonedas y monedas fiduciarias
     */
    CRIPTO
} 